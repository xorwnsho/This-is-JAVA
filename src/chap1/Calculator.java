package chap1;

public class Calculator {
    public static void main(String[] args) {
        // 변수 x 선언후 1 저장
        int x = 1;
        // 변수 y 선언 후 2 저장
        int y = 2;
        // 변수 result 선언 후 x와 y를 더한 값 저장
        int result = x + y;
        // 콘솔에 출력하는 메서드 호출
        System.out.println(result);
    }
}
